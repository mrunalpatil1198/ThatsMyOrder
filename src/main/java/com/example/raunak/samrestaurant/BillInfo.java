package com.example.raunak.samrestaurant;

public class BillInfo {

    String name;

    String price;

    public BillInfo() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
